<h2>Privacy and cookies</h2>

<p>Pledgebank is no longer an active site, so it’s not possible to create a
new account, sign a pledge or make any other kind of action.</p>
<p>However, the site still contains data from its original users. This page
explains our handling of that data.</p>

<h3>Who we are</h3>
<p>Pledgebank was built and run by <a
href="https://www.mysociety.org/">mySociety</a>, a UK not-for-profit social
enterprise. We still own and publish the site in its archived state.</p>

<h3>What data we hold</h3>
<p>If you made or signed a pledge on Pledgebank during the years it was running
(2005-2015), your name is held in our database. Your name is on the
publicly-accessible web pages of any pledge that you created and, if you did
not opt for anonymity, any pledge that you signed. No email addresses or phone
numbers are stored.</p>

<h3>Effects of publication on Pledgebank</h3>
<p>As the site is still public, you may find that Pledgebank pages appear in
results when your name is input into search engines.</p>

<h3>Lawful basis of processing</h3>
<p>Our lawful basis for holding this information is legitimate interests (this
is as laid out in 6(1)(f) – of the GDPR, in force from 25 May 2018). We believe
that we are pursuing a legitimate interest in processing your personal data to
produce this archive of the site and the pledges made and signed to the benefit
of society in making available this archive as an example of one of the first
conditional commitment sites made available on the internet.</p>

<h3>Retention periods</h3>
<p>We do not currently have any plans to take Pledgebank offline or to delete
any of the data it holds. However, see below.</p>

<h3>Your right to access</h3>
<p>You may contact us at any time to ask to see what personal data we hold
about you. Please contact us to request this.</p>

<h3>Your right to erasure</h3>
<p>If you choose to exercise your right to erasure, we can on request remove
your name from our systems. This effectively anonymises any pledges you have
made through the site, but retains the pledges themselves in our capacity as an
archive. Please contact us via the details at the foot of this page.

<h3>Your right to object</h3>
<p>The General Data Protection Regulation gives you the right to object to our
processing of your personal information and to ask us to stop processing it.
However, it also gives us the right to continue to process it if we can
demonstrate compelling legitimate grounds for the processing that override your
interests, rights and freedoms. To exercise your right to object, you can
contact us, giving specific reasons why you are objecting to the processing of
your personal data. These reasons should be based upon your particular
situation.</p>

<h3>Your right to complain</h3>
<p>If you believe that we have mishandled your data, you have the right to
lodge a complaint with the Information Commissioner’s Office. You can report a
concern here (but do contact us first, so that we can try and help).</p>

